#include <stdio.h>
#include <stdlib.h>
#include <math.h>


int main ()
{
    
    printf("1 + 1 = %d\n", 1 + 1);
    printf("1 - 1 = %d\n", 1 - 1);

    printf("3 * 1.5 = %f\n", 3 * 1.5);
    printf("3 / 2 = %f\n", 3.0 / 2);

    printf("floor(0.5) = %f\n", floor(0.5));
    printf("ceil(0.5) = %f\n", ceil(0.5));

    return 0;

}